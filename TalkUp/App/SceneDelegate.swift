class SceneDelegate: UIResponder, UIWindowSceneDelegate {

    var window: UIWindow?


    func scene(_ scene: UIScene, willConnectTo session: UISceneSession, options connectionOptions: UIScene.ConnectionOptions) {
        // Use this method to optionally configure and attach the UIWindow `window` to the provided UIWindowScene `scene`.
        // If using a storyboard, the `window` property will automatically be initialized and attached to the scene.
        // This delegate does not imply the connecting scene or session are new (see `application:configurationForConnectingSceneSession` instead).
        guard let windowScene = (scene as? UIWindowScene) else { return }
        let window = UIWindow(windowScene: windowScene)
                
        let onboardingVC = OnboardingBuilder.build()
        let onboardingNav = UINavigationController(rootViewController: onboardingVC)
        
        let homeController = MainTabBarController()
        let homeNav = UINavigationController(rootViewController: homeController)
        
        let rootViewController: UIViewController
        
        if Auth.auth().currentUser != nil {
            rootViewController = homeNav
        } else {
            rootViewController = onboardingNav
        }
        
        // UINavigationBar
        let navBarAppearance = UINavigationBarAppearance()
        navBarAppearance.configureWithOpaqueBackground()
        navBarAppearance.backgroundColor = .systemBackground
        navBarAppearance.shadowColor = nil
        navBarAppearance.titleTextAttributes = [.foregroundColor: UIColor.black]
        navBarAppearance.backgroundColor = .systemGray5

        UINavigationBar.appearance().standardAppearance = navBarAppearance
        UINavigationBar.appearance().scrollEdgeAppearance = navBarAppearance
        UINavigationBar.appearance().compactAppearance = navBarAppearance
        UINavigationBar.appearance().tintColor = .black
//        UINavigationBar.appearance().prefersLargeTitles = true

        // UITabBar
        let tabBarAppearance = UITabBarAppearance()
        tabBarAppearance.configureWithOpaqueBackground()
        tabBarAppearance.backgroundColor = .systemGray5

        UITabBar.appearance().standardAppearance = tabBarAppearance
        UITabBar.appearance().scrollEdgeAppearance = tabBarAppearance
        UITabBar.appearance().tintColor = .black
//        UINavigationBar.appearance().prefersLargeTitles = true
        window.rootViewController = rootViewController
        self.window = window
        window.makeKeyAndVisible()
    }

